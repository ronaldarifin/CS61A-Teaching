def add_numbers(a, b):
    """This function takes two numbers and returns their sum."""
    result = a + b
    return result

def add_number_no(a, b):
    """This function takes two numbers and prints their sum."""
    result = a + b
    print(result)

#Example
add_numbers(1, 2) # Evaluates to 3, printing nothing to Terminal
add_number_no(1, 2) # Evaluates to 3, printing 3 to Terminal